marks=int(input("Enter your marks:"))
if marks>=90:
    print("Grade A")
elif marks>=75:
    print("Grade B")
elif marks>=50:
    print("Grade C")
else:
    print("Grade D")       
# Simple BMI Checker
weight=int(input("Enter your weight:"))
if weight>=25:
    print("Overweight")
elif weight>=18:
    print("Normal")
else:
    print("UnderWeight")
